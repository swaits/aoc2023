#[inline]
pub fn digit_value(matched: &str) -> usize {
    match matched {
        "1" | "one" => 1,
        "2" | "two" => 2,
        "3" | "three" => 3,
        "4" | "four" => 4,
        "5" | "five" => 5,
        "6" | "six" => 6,
        "7" | "seven" => 7,
        "8" | "eight" => 8,
        "9" | "nine" => 9,
        "0" | "zero" => 0,
        _ => unreachable!(),
    }
}
